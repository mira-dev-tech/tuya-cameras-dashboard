const MIRA_LANG_KEY = 'mira_lang'
const MIRA_LANG_HTML = { en: 'en', pt: 'pt-BR', es: 'es' }

/** @type {Set<(lang: MiraLang) => void>} */
const miraLangListeners = new Set()

function miraGetLang() {
  const saved = localStorage.getItem(MIRA_LANG_KEY)
  if (saved && MIRA_LOCALES[saved]) return /** @type {MiraLang} */ (saved)
  return 'en'
}

function miraSetLang(lang) {
  if (!MIRA_LOCALES[lang]) return
  localStorage.setItem(MIRA_LANG_KEY, lang)
  document.documentElement.lang = MIRA_LANG_HTML[lang] || 'en'
  miraApplyI18n(document)
  miraLangListeners.forEach((fn) => fn(lang))
}

function miraT(key, vars = {}) {
  const lang = miraGetLang()
  let str = MIRA_LOCALES[lang]?.[key] ?? MIRA_LOCALES.en[key] ?? key
  Object.entries(vars).forEach(([k, v]) => {
    str = str.replaceAll(`{${k}}`, String(v))
  })
  return str
}

function miraApplyI18n(root = document) {
  root.querySelectorAll('[data-i18n]').forEach((el) => {
    const key = el.getAttribute('data-i18n')
    if (key) el.textContent = miraT(key)
  })
  root.querySelectorAll('[data-i18n-title]').forEach((el) => {
    const key = el.getAttribute('data-i18n-title')
    if (key) el.setAttribute('title', miraT(key))
  })
  root.querySelectorAll('[data-i18n-placeholder]').forEach((el) => {
    const key = el.getAttribute('data-i18n-placeholder')
    if (key) el.setAttribute('placeholder', miraT(key))
  })
  const sel = root.querySelector('#langSelect')
  if (sel && sel instanceof HTMLSelectElement) sel.value = miraGetLang()
  const titleKey = root.querySelector('meta[name="i18n-title"]')?.getAttribute('content')
  if (titleKey) document.title = miraT(titleKey)
}

function miraOnLangChange(fn) {
  miraLangListeners.add(fn)
  return () => miraLangListeners.delete(fn)
}

function miraInitLangSelect() {
  document.querySelectorAll('#langSelect').forEach((sel) => {
    if (!(sel instanceof HTMLSelectElement)) return
    sel.value = miraGetLang()
    sel.addEventListener('change', () => miraSetLang(sel.value))
  })
}

document.documentElement.lang = MIRA_LANG_HTML[miraGetLang()] || 'en'

function miraDateLocale() {
  return MIRA_LANG_HTML[miraGetLang()] || 'en-US'
}

window.MiraI18n = {
  getLang: miraGetLang,
  setLang: miraSetLang,
  t: miraT,
  apply: miraApplyI18n,
  onChange: miraOnLangChange,
  initLangSelect: miraInitLangSelect,
  dateLocale: miraDateLocale,
  tipKeys: ['loadTip1', 'loadTip2', 'loadTip3', 'loadTip4', 'loadTip5'],
}
